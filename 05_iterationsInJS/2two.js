// While loops

let index = 0
while (index <= 10) {
    // console.log(`value of index is ${index}`);
    index = index + 2
}

let myArray = ['batman', 'spiderman', 'superman']

let arr = 0
while (arr < myArray.length) {
    // console.log(`Value is ${myArray[arr]}`);
    arr++
}

// Do while loop

let score = 11

do {
    console.log(`Score is ${score}`); // in this, work is done first condition is checked later
    score ++
} while (score <= 10);